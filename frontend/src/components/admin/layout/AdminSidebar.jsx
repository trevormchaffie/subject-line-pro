const AdminSidebar = () => {
  const { sidebarOpen, toggleSidebar, isMobile } = useAdminLayout();
  const location = useLocation();

  // Overlay for mobile when sidebar is open
  const renderOverlay = () => {
    if (isMobile && sidebarOpen) {
      return (
        <div
          className="fixed inset-0 bg-black bg-opacity-50 z-10"
          onClick={toggleSidebar}
          aria-hidden="true"
        />
      );
    }
    return null;
  };

  return (
    <>
      {renderOverlay()}

      <aside
        className={`fixed top-0 left-0 z-20 h-full w-64 bg-gray-800 text-white transition-all duration-300 transform ${
          sidebarOpen ? "translate-x-0" : "-translate-x-full"
        } ${isMobile ? "mt-0" : "mt-14"}`}
      >
        {/* Sidebar header with close button (mobile only) */}
        {isMobile && (
          <div className="flex items-center justify-between p-4 border-b border-gray-700">
            <h2 className="text-xl font-bold">Admin Menu</h2>
            <button
              onClick={toggleSidebar}
              className="text-white focus:outline-none"
              aria-label="Close sidebar"
            >
              <svg
                className="h-6 w-6"
                fill="none"
                viewBox="0 0 24 24"
                stroke="currentColor"
              >
                <path
                  strokeLinecap="round"
                  strokeLinejoin="round"
                  strokeWidth={2}
                  d="M6 18L18 6M6 6l12 12"
                />
              </svg>
            </button>
          </div>
        )}

        {/* Navigation */}
        <nav className="mt-5 px-2">
          <ul className="space-y-2">
            {navItems.map((item) => (
              <li key={item.path}>
                <NavLink
                  to={item.path}
                  end={item.path === routes.admin.dashboard}
                  className={({ isActive }) => `
                    flex items-center px-4 py-3 rounded-md transition-colors
                    ${
                      isActive
                        ? "bg-primary text-white"
                        : "text-gray-300 hover:bg-gray-700"
                    }
                  `}
                  onClick={isMobile ? toggleSidebar : undefined}
                >
                  <span className="mr-3">{item.icon}</span>
                  <span>{item.label}</span>
                </NavLink>
              </li>
            ))}
          </ul>
        </nav>

        {/* Sidebar footer */}
        <div className="absolute bottom-0 w-full p-4 border-t border-gray-700">
          <p className="text-sm text-gray-400 text-center">
            &copy; {new Date().getFullYear()} Subject Line Pro
          </p>
        </div>
      </aside>
    </>
  );
};

export default AdminSidebar;
